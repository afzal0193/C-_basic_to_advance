#include<iostream>
#include<bits/stdc++.h>
using namespace std;
int main(){
    int day;
    cin>>day;
    if(day ==1){
        cout<<"saterday"<<endl;
    }
    else if(day ==2){
        cout<<"sunday"<<endl;
    }
    else if(day ==3){
        cout<<"munday"<<endl;
    }
    else if(day ==4){
        cout<<"tuseday"<<endl;
    }
    else if(day ==5){
        cout<<"wednesday"<<endl;
    }
    else if(day ==6){
        cout<<"Thrusday"<<endl;
    }
    else{
        cout<<"firday"<<endl;
    }
    return 0;
}
