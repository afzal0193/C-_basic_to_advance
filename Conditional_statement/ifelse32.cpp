#include<iostream>
#include<bits/stdc++.h>
using namespace std;
int main(){
    int a;
    cout<<"Enter a number witch range in 0-9: ";
    cin>>a;

    if (a==1){
        cout<<"One"<<endl;
    }
    else if(a==2){
        cout<<"Two"<<endl;
    }
    else if(a==3){
        cout<<"Three"<<endl;
    }
    else if(a ==4){
        cout<<"Four"<<endl;
    }
    else if(a ==5){
        cout<<"Five"<<endl;
    }
    else if(a==6){
        cout<<"six"<<endl;
    }
    else if(a ==7){
        cout<<"seven"<<endl;
    }
    else if(a ==8){
        cout<<"Eight"<<endl;
    }
    else if(a ==9){
        cout<<"Nine"<<endl;
    }
    else{
        cout<<"Zero"<<endl;
    }

    return 0;
}
